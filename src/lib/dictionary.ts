export type Dictionary = typeof dictionary;
export type Locale = "vi" | "en";

/**
 * Helper to resolve a nested dictionary path by joining keys with dots.
 * e.g., resolve(dictionary, "hero.badge", "vi")
 */
export function resolveDictionary(
  dict: Dictionary,
  path: string,
  locale: Locale,
): string | string[] {
  const keys = path.split(".");
  let result: unknown = dict;
  for (const key of keys) {
    if (result && typeof result === "object" && key in result) {
      result = (result as Record<string, unknown>)[key];
    } else {
      console.warn(`Dictionary key not found: ${path}`);
      return path;
    }
  }
  if (result && typeof result === "object" && locale in (result as Record<string, unknown>)) {
    return (result as Record<string, string | string[]>)[locale];
  }
  console.warn(`Locale not found for key: ${path}[${locale}]`);
  return path;
}